package BT3_IfElse;

public class Thamso {
    static void thamso(int n) {
        if (n == 20) {
            System.out.println("n bằng 20");
        } else if (n > 20) {
            System.out.println("n lớn hơn 20");
        } else if (n < 20) {
            System.out.println("n nhỏ hơn 20");
        }

    }

    public static void main(String[] args) {
        thamso(30);

    }
}

